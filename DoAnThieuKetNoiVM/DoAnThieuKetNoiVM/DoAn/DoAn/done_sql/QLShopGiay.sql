CREATE TABLE TAI_KHOAN (
    MaTK VARCHAR(20) PRIMARY KEY,
    MaNV VARCHAR(20) NULL,
    TenDangNhap VARCHAR(50) UNIQUE NOT NULL,
    Salt UNIQUEIDENTIFIER DEFAULT NEWID(),
    MatKhau VARBINARY(MAX) NOT NULL,
    MaVT VARCHAR(10),
    IsDeleted BIT NOT NULL DEFAULT 0, 
    FOREIGN KEY (MaNV) REFERENCES NHAN_VIEN(MaNV),
    FOREIGN KEY (MaVT) REFERENCES VAI_TRO(MaVT)
);

CREATE TABLE YEU_CAU_DANG_KY (
    MaYeuCau INT PRIMARY KEY IDENTITY(1,1),
    TenDangNhap NVARCHAR(50) NOT NULL UNIQUE,
    HoTen NVARCHAR(100) NOT NULL,
    MatKhau VARBINARY(MAX) NOT NULL,
    Salt UNIQUEIDENTIFIER NOT NULL,
    MaNV NVARCHAR(20) NULL,
    NgayTao DATETIME DEFAULT GETDATE(),
    TrangThai NVARCHAR(20) DEFAULT N'ChoXet',
    GhiChu NVARCHAR(500),
    Email NVARCHAR(100) NULL
);

CREATE TABLE KHO (
    MaKho VARCHAR(10) PRIMARY KEY,
    TenKho NVARCHAR(100) NOT NULL,
    DiaChi NVARCHAR(MAX)
);

CREATE TABLE TON_KHO (
    MaKho VARCHAR(10),
    MaBienThe INT,
    SoLuongTon INT DEFAULT 0,
    PRIMARY KEY (MaKho, MaBienThe),
    CONSTRAINT CK_TonKho_KhongAm CHECK (SoLuongTon >= 0),
    FOREIGN KEY (MaKho) REFERENCES KHO(MaKho),
    FOREIGN KEY (MaBienThe) REFERENCES BIEN_THE_SP(MaBienThe)
);

CREATE TABLE PHIEU_NHAP (
    MaPN VARCHAR(20) PRIMARY KEY,
    NgayNhap DATETIME DEFAULT GETDATE(),
    MaKho VARCHAR(10),
    MaNSX VARCHAR(10),
    FOREIGN KEY (MaKho) REFERENCES KHO(MaKho),
    FOREIGN KEY (MaNSX) REFERENCES NHA_SAN_XUAT(MaNSX)
);

CREATE TABLE CT_PHIEU_NHAP (
    MaPN VARCHAR(20),
    MaBienThe INT,
    SoLuong INT CHECK (SoLuong > 0),
    GiaNhap DECIMAL(18,2) CHECK (GiaNhap >= 0),
    PRIMARY KEY (MaPN, MaBienThe),
    FOREIGN KEY (MaPN) REFERENCES PHIEU_NHAP(MaPN),
    FOREIGN KEY (MaBienThe) REFERENCES BIEN_THE_SP(MaBienThe)
);

CREATE TABLE KHACH_HANG (
    MaKH VARCHAR(20) PRIMARY KEY,
    TenKH NVARCHAR(100) NOT NULL,
    SDT VARCHAR(15),
    DiaChi NVARCHAR(MAX)
);

CREATE TABLE HOA_DON (
    MaHD VARCHAR(20) PRIMARY KEY,
    NgayLap DATETIME DEFAULT GETDATE(),
    MaKH VARCHAR(20),
    MaTK VARCHAR(20),
    MaKho VARCHAR(10),
    MaPT INT,
    TongTien DECIMAL(18,2) DEFAULT 0,
    TrangThai NVARCHAR(50),
    FOREIGN KEY (MaKH) REFERENCES KHACH_HANG(MaKH),
    FOREIGN KEY (MaTK) REFERENCES TAI_KHOAN(MaTK),
    FOREIGN KEY (MaKho) REFERENCES KHO(MaKho),
    FOREIGN KEY (MaPT) REFERENCES PHUONG_THUC_TT(MaPT)
);

CREATE TABLE CT_HOA_DON (
    MaHD VARCHAR(20),
    MaBienThe INT,
    SoLuong INT CHECK (SoLuong > 0),
    GiaBan DECIMAL(18,2),
    PRIMARY KEY (MaHD, MaBienThe),
    FOREIGN KEY (MaHD) REFERENCES HOA_DON(MaHD),
    FOREIGN KEY (MaBienThe) REFERENCES BIEN_THE_SP(MaBienThe)
);
