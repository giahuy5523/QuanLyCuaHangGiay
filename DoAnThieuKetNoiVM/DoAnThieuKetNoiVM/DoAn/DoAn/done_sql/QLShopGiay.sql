CREATE OR ALTER TRIGGER TRG_UpdateKho_Nhap ON CT_PHIEU_NHAP AFTER INSERT AS
BEGIN
    SET NOCOUNT ON;
    INSERT INTO TON_KHO (MaKho, MaBienThe, SoLuongTon)
    SELECT DISTINCT PN.MaKho, i.MaBienThe, 0
    FROM inserted i
    JOIN PHIEU_NHAP PN ON i.MaPN = PN.MaPN
    WHERE NOT EXISTS (SELECT 1 FROM TON_KHO TK WHERE TK.MaKho = PN.MaKho AND TK.MaBienThe = i.MaBienThe);

    UPDATE TK SET TK.SoLuongTon = TK.SoLuongTon + sub.TongNhap
    FROM TON_KHO TK
    INNER JOIN (
        SELECT PN.MaKho, i.MaBienThe, SUM(i.SoLuong) as TongNhap
        FROM inserted i JOIN PHIEU_NHAP PN ON i.MaPN = PN.MaPN GROUP BY PN.MaKho, i.MaBienThe
    ) sub ON TK.MaKho = sub.MaKho AND TK.MaBienThe = sub.MaBienThe;
END;
GO

CREATE OR ALTER TRIGGER TRG_UpdateKho_Xuat ON CT_HOA_DON AFTER INSERT AS
BEGIN
    SET NOCOUNT ON;
    IF EXISTS (
        SELECT 1 FROM inserted i
        JOIN HOA_DON HD ON i.MaHD = HD.MaHD
        JOIN TON_KHO TK ON TK.MaKho = HD.MaKho AND TK.MaBienThe = i.MaBienThe
        WHERE TK.SoLuongTon < i.SoLuong
    )
    BEGIN
        RAISERROR(N'Lỗi: Không đủ số lượng tồn kho!', 16, 1);
        ROLLBACK TRANSACTION; RETURN;
    END

    UPDATE TK SET TK.SoLuongTon = TK.SoLuongTon - i.SoLuong
    FROM TON_KHO TK
    JOIN inserted i ON TK.MaBienThe = i.MaBienThe
    JOIN HOA_DON HD ON i.MaHD = HD.MaHD AND TK.MaKho = HD.MaKho;
END;
GO

CREATE OR ALTER TRIGGER TRG_UpdateTongTien ON CT_HOA_DON AFTER INSERT, UPDATE, DELETE AS
BEGIN
    SET NOCOUNT ON;
    UPDATE HOA_DON
    SET TongTien = (SELECT COALESCE(SUM(SoLuong * GiaBan), 0) FROM CT_HOA_DON WHERE MaHD = HOA_DON.MaHD)
    WHERE MaHD IN (SELECT MaHD FROM inserted UNION SELECT MaHD FROM deleted);
END;
GO

