CREATE TABLE LOAI_HANG (
    MaLoai VARCHAR(10) PRIMARY KEY,
    TenLoai NVARCHAR(100) NOT NULL
);

CREATE TABLE NHA_SAN_XUAT (
    MaNSX VARCHAR(10) PRIMARY KEY,
    TenNSX NVARCHAR(100) NOT NULL,
