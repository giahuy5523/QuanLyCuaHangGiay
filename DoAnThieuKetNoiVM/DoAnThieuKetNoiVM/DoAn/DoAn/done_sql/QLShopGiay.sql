CREATE TABLE VAI_TRO (
    MaVT VARCHAR(10) PRIMARY KEY,
    TenVT NVARCHAR(50) NOT NULL
);

CREATE TABLE SAN_PHAM (
    MaSP VARCHAR(20) PRIMARY KEY,
    TenSP NVARCHAR(200) NOT NULL,
    MaLoai VARCHAR(10),
    MaNSX VARCHAR(10),
    MaDVT VARCHAR(10),
    GiaBan DECIMAL(18,2) DEFAULT 0,
    IsDeleted BIT DEFAULT 0,
    CONSTRAINT CK_GiaBan CHECK (GiaBan >= 0),
    FOREIGN KEY (MaLoai) REFERENCES LOAI_HANG(MaLoai),
    FOREIGN KEY (MaNSX) REFERENCES NHA_SAN_XUAT(MaNSX),
    FOREIGN KEY (MaDVT) REFERENCES DON_VI_TINH(MaDVT)
