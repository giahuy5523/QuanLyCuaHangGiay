CREATE TABLE ANH_SP (
    MaAnh INT IDENTITY(1,1) PRIMARY KEY,
    MaSP VARCHAR(20),
    DuongDan NVARCHAR(255) NOT NULL,
    LaAnhChinh BIT DEFAULT 0,
    FOREIGN KEY (MaSP) REFERENCES SAN_PHAM(MaSP) ON DELETE CASCADE
