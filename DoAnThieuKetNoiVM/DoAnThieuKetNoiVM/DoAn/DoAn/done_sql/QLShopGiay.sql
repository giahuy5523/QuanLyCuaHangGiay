CREATE TABLE BIEN_THE_SP (
    MaBienThe INT IDENTITY(1,1) PRIMARY KEY,
    MaSP VARCHAR(20),
    MaSize VARCHAR(10),
    MaMau VARCHAR(10),
    SKU VARCHAR(50) UNIQUE,
    IsDeleted BIT DEFAULT 0,
    CONSTRAINT UQ_BienThe_Logic UNIQUE (MaSP, MaSize, MaMau),
    FOREIGN KEY (MaSP) REFERENCES SAN_PHAM(MaSP),
    FOREIGN KEY (MaSize) REFERENCES SIZE_GIAY(MaSize),
    FOREIGN KEY (MaMau) REFERENCES MAU_SAC(MaMau)
