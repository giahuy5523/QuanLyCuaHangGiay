CREATE TABLE NHAN_VIEN (
    MaNV VARCHAR(20) PRIMARY KEY,
    HoTen NVARCHAR(100) NOT NULL,
    NgaySinh DATE,
    Email VARCHAR(100) UNIQUE,
    CONSTRAINT CK_Email_Format CHECK (Email LIKE '%_@_%._%'),
    IsDeleted BIT DEFAULT 0
